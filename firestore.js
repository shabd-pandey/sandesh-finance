/**
 * deleteLoan(userId, loanId)
 *
 * Deletes a loan document. If the loan was still 'active', also
 * reverses the investor and borrower counters in a batch.
 *
 * ⚠  Always fetch the loan first to check its status.
 *
 * EXAMPLE
 *   const loan = (await getLoans(uid)).find(l => l.id === loanId);
 *   await deleteLoan(uid, loanId, loan);
 */
export async function deleteLoan(userId, loanId, loanData) {
    try {
        const batch = writeBatch(db);

        // 1 — Delete the loan document
        batch.delete(ref(userId, "loans", loanId));

        // 2 — If it was active, reverse the counters
        if (loanData?.status === "active") {
            batch.update(ref(userId, "investors", loanData.investorId), {
                invested:    increment(-Number(loanData.amount)),
                activeLoans: increment(-1),
                updatedAt:   serverTimestamp()
            });
            batch.update(ref(userId, "borrowers", loanData.borrowerId), {
                borrowed:    increment(-Number(loanData.amount)),
                activeLoans: increment(-1),
                updatedAt:   serverTimestamp()
            });
        }

        await batch.commit();
        console.log("✅ deleteLoan →", loanId);
    } catch (err) {
        console.error("❌ deleteLoan:", err);
        throw err;
    }
}


