/**
 * loadAllData(userId)
 *
 * Fetches all collections in parallel and returns an appData-shaped
 * object that is a drop-in replacement for the existing localStorage
 * appData. Wire this into the DOMContentLoaded block in script.js
 * as part of Step 3 (localStorage → Firestore migration).
 *
 * @returns {Promise<{
 *   investors: Array,
 *   borrowers: Array,
 *   loans:     Array,
 *   totalCommissionEarned: number,
 *   dismissedAlerts: Object,
 *   delays: Array
 * }>}
 *
 * EXAMPLE — replacing loadData() in script.js:
 *
 *   import { loadAllData } from './firestore.js';
 *   import { onAuthChange } from './auth.js';
 *
 *   onAuthChange(async (user) => {
 *       if (!user) { window.location.href = 'login.html'; return; }
 *
 *       appData = await loadAllData(user.uid);
 *       renderInvestors();
 *       renderBorrowers();
 *       renderLoans();
 *       updateStats();
 *       updateCapitalOverview();
 *   });
 */
export async function loadAllData(userId) {
    try {
        const [investors, borrowers, loans, metaDoc] = await Promise.all([
            getInvestors(userId),
            getBorrowers(userId),
            getLoans(userId),
            getAppMeta(userId)
        ]);

        return {
            investors,
            borrowers,
            loans,
            totalCommissionEarned: metaDoc.totalCommissionEarned ?? 0,
            dismissedAlerts:       metaDoc.dismissedAlerts       ?? {},
            delays:                []   // delays are embedded inside each loan document
        };
    } catch (err) {
        console.error("❌ loadAllData:", err);
        throw err;
    }
}


