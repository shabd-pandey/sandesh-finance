/**
 * getAppMeta(userId)
 *
 * Returns the meta document for this user.
 * Contains: totalCommissionEarned, dismissedAlerts, updatedAt.
 *
 * Returns a safe default object if the document doesn't exist yet.
 *
 * EXAMPLE
 *   const meta = await getAppMeta(uid);
 *   appData.totalCommissionEarned = meta.totalCommissionEarned;
 *   appData.dismissedAlerts       = meta.dismissedAlerts;
 */
export async function getAppMeta(userId) {
    try {
        const snap = await getDoc(meta(userId));
        if (snap.exists()) {
            return snap.data();
        }
        // First-time user — return safe defaults
        return { totalCommissionEarned: 0, dismissedAlerts: {} };
    } catch (err) {
        console.error("❌ getAppMeta:", err);
        throw err;
    }
}

// ----------------------------------------------------------------

/**
 * updateAppMeta(userId, updates)
 *
 * Partial update for the meta document.
 * Uses { merge: true } so new keys don't overwrite the whole doc.
 *
 * EXAMPLE — save dismissed alert keys:
 *   await updateAppMeta(uid, { dismissedAlerts: appData.dismissedAlerts });
 *
 * EXAMPLE — manually set commission total:
 *   await updateAppMeta(uid, { totalCommissionEarned: 250000 });
 */
export async function updateAppMeta(userId, updates) {
    try {
        await setDoc(meta(userId), {
            ...updates,
            updatedAt: serverTimestamp()
        }, { merge: true });
        console.log("✅ updateAppMeta");
    } catch (err) {
        console.error("❌ updateAppMeta:", err);
        throw err;
    }
}


