/**
 * addInvestor(userId, investorData)
 *
 * Writes a new investor document under users/{userId}/investors.
 * The document ID is auto-generated by Firestore.
 *
 * @param {string} userId        — from auth.getCurrentUser().uid
 * @param {Object} investorData  — shape must match:
 *   {
 *     name:        string,   // full name
 *     phone:       string,   // 10-digit
 *     capital:     number,   // total capital available (₹)
 *     invested:    number,   // currently deployed (default 0)
 *     activeLoans: number    // count of active loans (default 0)
 *   }
 *
 * @returns {Promise<string>}   — the new Firestore document ID
 *
 * EXAMPLE
 *   const id = await addInvestor(uid, { name:"Ravi", phone:"9876543210", capital:500000, invested:0, activeLoans:0 });
 */
export async function addInvestor(userId, investorData) {
    try {
        const payload = {
            name:        investorData.name.trim(),
            phone:       investorData.phone.trim(),
            capital:     Number(investorData.capital),
            invested:    Number(investorData.invested    ?? 0),
            activeLoans: Number(investorData.activeLoans ?? 0),
            createdAt:   serverTimestamp()
        };

        const docRef = await addDoc(col(userId, "investors"), payload);
        console.log("✅ addInvestor →", docRef.id);
        return docRef.id;
    } catch (err) {
        console.error("❌ addInvestor:", err);
        throw err;
    }
}

