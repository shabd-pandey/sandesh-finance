/**
 * updateLoanStatus(userId, loanId, newStatus)
 *
 * Marks a loan as 'completed' or 'defaulted' and atomically
 * reverses the investor/borrower counters in the same batch.
 * Also increments totalCommissionEarned on the meta document.
 *
 * @param {string} newStatus  — 'completed' | 'defaulted'
 *
 * EXAMPLE
 *   await updateLoanStatus(uid, loanId, 'completed');
 */
export async function updateLoanStatus(userId, loanId, newStatus) {
    try {
        // Fetch the loan to read amount and commissionAmount
        const loanSnap = await getDoc(ref(userId, "loans", loanId));
        if (!loanSnap.exists()) throw new Error(`Loan ${loanId} not found`);

        const loan  = loanSnap.data();
        const batch = writeBatch(db);

        // 1 — Update loan status
        batch.update(ref(userId, "loans", loanId), {
            status:        newStatus,
            completedDate: new Date().toISOString().split("T")[0],
            updatedAt:     serverTimestamp()
        });

        // 2 — Reverse investor counters
        batch.update(ref(userId, "investors", loan.investorId), {
            invested:    increment(-Number(loan.amount)),
            activeLoans: increment(-1),
            updatedAt:   serverTimestamp()
        });

        // 3 — Reverse borrower counters
        batch.update(ref(userId, "borrowers", loan.borrowerId), {
            borrowed:    increment(-Number(loan.amount)),
            activeLoans: increment(-1),
            updatedAt:   serverTimestamp()
        });

        // 4 — Accumulate commission in meta document (upsert with merge)
        if (newStatus === "completed") {
            batch.set(meta(userId), {
                totalCommissionEarned: increment(Number(loan.commissionAmount ?? 0)),
                updatedAt: serverTimestamp()
            }, { merge: true });
        }

        await batch.commit();
        console.log(`✅ updateLoanStatus → ${loanId} is now ${newStatus}`);
    } catch (err) {
        console.error("❌ updateLoanStatus:", err);
        throw err;
    }
}

