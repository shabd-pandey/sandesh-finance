/**
 * addLoan(userId, loanData)
 *
 * Writes the loan document AND atomically updates the referenced
 * investor and borrower counters in a single Firestore batch —
 * ensuring data stays consistent even if the browser closes mid-way.
 *
 * @param {Object} loanData — full shape from loanForm submit:
 *   {
 *     investorId:       string,
 *     borrowerId:       string,
 *     investorName:     string,
 *     borrowerName:     string,
 *     amount:           number,
 *     borrowerRate:     number,   // % the borrower pays
 *     investorRate:     number,   // % the investor earns
 *     frequency:        string,   // 'daily'|'weekly'|'15days'|'monthly'|'yearly'
 *     duration:         number,
 *     durationUnit:     string,   // 'days'|'weeks'|'months'|'years'
 *     durationInMonths: number,
 *     startDate:        string,   // "YYYY-MM-DD"
 *     dueDate:          string,   // "YYYY-MM-DD" — calculated before calling
 *     totalInterest:    number,
 *     investorShare:    number,
 *     commissionAmount: number,
 *     totalPayable:     number,
 *     status:           'active'
 *   }
 *
 * @returns {Promise<string>}  — new loan document ID
 */
export async function addLoan(userId, loanData) {
    try {
        const batch     = writeBatch(db);

        // 1 — Loan document (new auto-ID doc)
        const loanDocRef = doc(col(userId, "loans"));
        batch.set(loanDocRef, {
            investorId:       loanData.investorId,
            borrowerId:       loanData.borrowerId,
            investorName:     loanData.investorName,
            borrowerName:     loanData.borrowerName,
            amount:           Number(loanData.amount),
            borrowerRate:     Number(loanData.borrowerRate),
            investorRate:     Number(loanData.investorRate),
            frequency:        loanData.frequency,
            duration:         Number(loanData.duration),
            durationUnit:     loanData.durationUnit,
            durationInMonths: Number(loanData.durationInMonths),
            startDate:        loanData.startDate,
            dueDate:          loanData.dueDate,
            totalInterest:    Number(loanData.totalInterest),
            investorShare:    Number(loanData.investorShare),
            commissionAmount: Number(loanData.commissionAmount),
            totalPayable:     Number(loanData.totalPayable),
            status:           "active",
            delays:           [],          // populated by addLoanDelay()
            createdAt:        serverTimestamp()
        });

        // 2 — Update investor: increase invested + activeLoans counter
        batch.update(ref(userId, "investors", loanData.investorId), {
            invested:    increment(Number(loanData.amount)),
            activeLoans: increment(1),
            updatedAt:   serverTimestamp()
        });

        // 3 — Update borrower: increase borrowed + activeLoans counter
        batch.update(ref(userId, "borrowers", loanData.borrowerId), {
            borrowed:    increment(Number(loanData.amount)),
            activeLoans: increment(1),
            updatedAt:   serverTimestamp()
        });

        await batch.commit();
        console.log("✅ addLoan →", loanDocRef.id);
        return loanDocRef.id;
    } catch (err) {
        console.error("❌ addLoan:", err);
        throw err;
    }
}

